public class VarTest 
{
	public static void main(String[] args) 
	{
		int age;	    //변수 선언
		age = 25;	//변수 초기화 (값의 할당)
		System.out.println("당신의 나이는 " + age + "입니다.");

		String carName;
		carName = "쏘나타";
		System.out.println("당신의 차이름은 " + carName + "입니다.");

		char gender;
		gender = '여';
		System.out.println("당신의 성별은 " +gender + "입니다.");

		gender = '남';
		System.out.println("당신의 성별은 " +gender + "입니다.");

	}
}
